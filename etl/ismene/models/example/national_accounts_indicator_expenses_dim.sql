SELECT DISTINCT na_item, WHEN na_item = 'P2' THEN 'Intermediate consumption'
WHEN na_item = 'P3' THEN 'Final consumption expenditure'
WHEN na_item = 'P31' THEN 'Individual consumption expenditure'
WHEN na_item = 'P32' THEN 'Collective consumption expenditure'
WHEN na_item = 'P5' THEN 'Gross capital formation'
WHEN na_item = 'P51G' THEN 'Gross fixed capital formation'
WHEN na_item = 'D1' THEN 'Compensation of employees'
WHEN na_item = 'D3' THEN 'Subsidies'
WHEN na_item = 'P2_D29_D5_D8' THEN 'Intermediate consumption; other taxes on production; current taxes on income, wealth, etc.; adjustment for the change in pension entitlements'
WHEN na_item = 'D29_D5_D8' THEN 'Other taxes on production; current taxes on income, wealth, etc; adjustment for the change in pension entitlements'
WHEN na_item = 'D4' THEN 'Property income'
WHEN na_item = 'D4_S1311' THEN 'Property income, of which payable to subsector S1311'
WHEN na_item = 'D4_S1312' THEN 'Property income, of which payable to subsector S1312'
WHEN na_item = 'D4_S1313' THEN 'Property income, of which payable to subsector S1313'
WHEN na_item = 'D4_S1314' THEN 'Property income, of which payable to subsector S1314'
WHEN na_item = 'D62_D632' THEN 'Social benefits other than social transfers in kind and social transfers in kind - purchased market production'
WHEN na_item = 'D62' THEN 'Social benefits other than social transfers in kind'
WHEN na_item = 'D632' THEN 'Social transfers in kind - purchased market production'
WHEN na_item = 'D7' THEN 'Other current transfers'
WHEN na_item = 'D7_S1311' THEN 'Other current transfers,of which payable to subsector S1311'
WHEN na_item = 'D7_S1312' THEN 'Other current transfers,of which payable to subsector S1312'
WHEN na_item = 'D7_S1313' THEN 'Other current transfers,of which payable to subsector S1313'
WHEN na_item = 'D7_S1314' THEN 'Other current transfers,of which payable to subsector S1314'
WHEN na_item = 'D9' THEN 'Capital transfers'
WHEN na_item = 'D92' THEN 'Investment grants'
WHEN na_item = 'D9_S1311' THEN 'Capital transfers, of which payable to subsector S1311'
WHEN na_item = 'D9_S1312' THEN 'Capital transfers, of which payable to subsector S1312'
WHEN na_item = 'D9_S1313' THEN 'Capital transfers, of which payable to subsector S1313'
WHEN na_item = 'D9_S1314' THEN 'Capital transfers, of which payable to subsector S1314'
WHEN na_item = 'OP5ANP' THEN 'Gross capital formation and acquisitions less disposals of non-financial non-produced assets'
WHEN na_item = 'NP' THEN 'Acquisitions less disposals of non-financial non-produced assets'
WHEN na_item = 'TE' THEN 'Total general government expenditure'
FROM eurostat_stag.gov_10a_exp_table